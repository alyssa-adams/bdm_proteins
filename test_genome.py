def bdm_slidingwindow(sequence, window_size, sliding_jump):

    '''
    Give it a window size, and then calculate the bdm for each window
    :param sequence: ATCGetc
    :param window_size: the size of the sliding window
    :param sliding_jump: bp between windows
    :return: A list of BDM values
    '''

    sliding_jump = 1

    bdms = []
    for window in range(len(sequence) - window_size + 1):
        slice = sequence[window:window+window_size]
        measure = bdm.bdm(slice, normalized=True)
        bdms.append(measure)

    # delete last value since it's most likely a fragment of a window
    # Only if sliding jump is greater than 1

    return bdms


def random_slidingwindow(sequence, window_size, sliding_jump):

    '''
    Give it a window size, and then calculate the bdm for each window
    :param sequence: ATCGetc
    :param window_size: the size of the sliding window
    :param sliding_jump: bp between windows
    :return: A list of BDM values
    '''

    sliding_jump = 1

    randoms = []
    for window in range(len(sequence) - window_size + 1):
        slice = sequence[window:window+window_size]
        random.shuffle(slice)
        measure = bdm.bdm(slice, normalized=True)
        randoms.append(measure)

    # delete last value since it's most likely a fragment of a window
    # Only if sliding jump is greater than 1

    return randoms


